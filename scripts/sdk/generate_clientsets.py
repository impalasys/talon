HEADER = "Code generated by scripts/sdk/generate_clientsets.py. DO NOT EDIT."


@dataclass(frozen=True)
class Service:
    name: str
    domain: str
    field: str
    accessor: str
    proto_stem: str


def main() -> None:
    services = parse_services([PROTO_DIR / file for file in V1_PROTO_FILES])
    generate_go(services)
    generate_js(services)
    generate_python(services)
    generate_python_exports()
    generate_python_init()
    generate_java(services)


def parse_services(paths: list[Path]) -> list[Service]:
    services: list[Service] = []
    for path in paths:
        source = strip_proto_comments(path.read_text(encoding="utf-8"))
        for match in re.finditer(r"^\s*service\s+(\w+Service)\s+\{", source, re.MULTILINE):
            name = match.group(1)
            domain = name.removesuffix("Service")
            field = service_field_name(domain)
            services.append(
                Service(
                    name=name,
                    domain=domain,
                    field=field,
                    accessor=to_pascal_case(field),
                    proto_stem=path.stem,
                )
            )
    if not services:
        raise RuntimeError(f"no services found in {PROTO_DIR}")
    return services


def generate_go(services: list[Service]) -> None:
    field_width = max(len(service.field) for service in services + [Service("", "", "close", "", "")])
    init_key_width = field_width + 1
    lines = [
        f"// {HEADER}",
        "",
        "package talonclient",
        "",
        "import (",
        '\ttalonv1 "github.com/impalasys/talon/sdk/go/talon-client/talon/v1"',
        '\t"google.golang.org/grpc"',
        ")",
        "",
        "type Clientset struct {",
    ]
    for service in services:
        lines.append(f"\t{service.field:<{field_width}} talonv1.{service.name}Client")
    lines.append(f"\t{'close':<{field_width}} func() error")
    lines.extend(["}", ""])
    for service in services:
        lines.extend(
            [
                f"func (c *Clientset) {service.accessor}() talonv1.{service.name}Client {{",
                f"\treturn c.{service.field}",
                "}",
                "",
            ]
        )
    lines.extend(
        [
            "func newClientset(conn grpc.ClientConnInterface, close func() error) *Clientset {",
            "\treturn &Clientset{",
        ]
    )
    for service in services:
        key = f"{service.field}:"
        lines.append(f"\t\t{key:<{init_key_width}} talonv1.New{service.name}Client(conn),")
    lines.extend([f"\t\t{'close:':<{init_key_width}} close,", "\t}", "}", ""])
    write(GO_OUT, "\n".join(lines))


def generate_js(services: list[Service]) -> None:
    lines = [
        f"// {HEADER}",
        "",
        'import { createPromiseClient, type PromiseClient, type Transport } from "@connectrpc/connect";',
    ]
    for service in services:
        lines.append(
            f'import {{ {service.name} }} from "./gen/proto/talon/v1/{service.proto_stem}_connect.js";'
        )
    lines.extend(["", "export type TalonClient = {"])
    for service in services:
        lines.append(f"  {service.field}: PromiseClient<typeof {service.name}>;")
    lines.extend(["};", "", "export function createTalonClientset(transport: Transport): TalonClient {", "  return {"])
    for service in services:
        lines.append(f"    {service.field}: createPromiseClient({service.name}, transport),")
    lines.extend(["  };", "}", ""])
    write(JS_OUT, "\n".join(lines))


def generate_python(services: list[Service]) -> None:
    lines = [
        f"# {HEADER}",
        "",
        "import grpc",
        "",
    ]
    for service in services:
        lines.append(
            f"from talon_client.proto.talon.v1 import {service.proto_stem}_pb2_grpc"
        )
    lines.extend(
        [
            "",
            "",
        ]
    )
    lines.extend([
        "class TalonClient:",
    ])
    for service in services:
        lines.append(
            f"    {service.field}: {service.proto_stem}_pb2_grpc.{service.name}Stub"
        )
    lines.extend([
        "",
        "    def __init__(self, channel: grpc.Channel):",
    ])
    for service in services:
        lines.append(
            f"        self.{service.field} = {service.proto_stem}_pb2_grpc.{service.name}Stub(channel)"
        )
    lines.append("")
    write(PY_OUT, "\n".join(lines))


def generate_python_exports() -> None:
    for module_name, proto_paths in PY_EXPORT_GROUPS.items():
        lines = [
            f"# {HEADER}",
            "",
        ]
        exported_names: list[str] = []
        for proto_path in proto_paths:
            names = parse_python_export_names(ROOT / proto_path)
            if not names:
                continue
            imported_names = ", ".join(names)
            lines.append(f"from {python_pb2_module(proto_path)} import {imported_names}")
            exported_names.extend(names)
        lines.extend(["", "__all__ = ["])
        for name in exported_names:
            lines.append(f'    "{name}",')
        lines.extend(["]", ""])
        write(PY_PACKAGE_DIR / f"{module_name}.py", "\n".join(lines))


