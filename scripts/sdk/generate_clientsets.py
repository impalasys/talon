def parse_python_export_names(path: Path) -> list[str]:
    source = strip_proto_comments(path.read_text(encoding="utf-8"))
    brace_source = strip_proto_string_literals(source)
    names: list[str] = []
    for match in re.finditer(r"^\s*(message|enum)\s+(\w+)\s*(?:\{|$)", source, re.MULTILINE):
        if proto_brace_depth(brace_source, match.start()) != 0:
            continue
        names.append(match.group(2))
        if match.group(1) == "enum":
            names.extend(parse_enum_values(source, match.start()))
    return names


def proto_brace_depth(source: str, end: int) -> int:
    depth = 0
    for char in source[:end]:
        if char == "{":
            depth += 1
        elif char == "}":
            depth = max(0, depth - 1)
    return depth


def strip_proto_comments(source: str) -> str:
    source = re.sub(r"/\*.*?\*/", "", source, flags=re.DOTALL)
    return re.sub(r"//.*", "", source)


def strip_proto_string_literals(source: str) -> str:
    result: list[str] = []
    quote: str | None = None
    escaped = False
    for char in source:
        if quote:
            if escaped:
                escaped = False
            elif char == "\\":
                escaped = True
            elif char == quote:
                quote = None
            result.append("\n" if char == "\n" else " ")
            continue
        if char in {'"', "'"}:
            quote = char
            result.append(" ")
        else:
            result.append(char)
    return "".join(result)


def parse_enum_values(source: str, start: int) -> list[str]:
    body_start = source.find("{", start)
    if body_start == -1:
        return []
    depth = 0
    for index in range(body_start, len(source)):
        char = source[index]
        if char == "{":
            depth += 1
        elif char == "}":
            depth -= 1
            if depth == 0:
                body = source[body_start + 1 : index]
                return [
                    match.group(1)
                    for match in re.finditer(r"^\s*([a-zA-Z_][a-zA-Z0-9_]*)\s*=", body, re.MULTILINE)
                    if match.group(1) != "option"
                ]
    return []


def python_pb2_module(proto_path: str) -> str:
    path = Path(proto_path)
    parts = path.with_suffix("").parts
    if parts[0] != "proto":
        raise ValueError(f"unexpected proto path: {proto_path}")
    return ".".join(["talon_client", "proto", *parts[1:-1], f"{parts[-1]}_pb2"])


def generate_java(services: list[Service]) -> None:
    lines = [
        f"// {HEADER}",
        "",
        "package systems.impala.talon.client;",
        "",
        "import io.grpc.Channel;",
    ]
    for service in services:
        lines.append(f"import talon.v1.{service.name}Grpc;")
    lines.extend(
        [
            "",
            "public final class TalonClientset {",
            "    private final Channel channel;",
        ]
    )
    for service in services:
        lines.append(
            f"    private final {service.name}Grpc.{service.name}BlockingStub {service.field};"
        )
        lines.append(
            f"    private final {service.name}Grpc.{service.name}Stub {service.field}Async;"
        )
        lines.append(
            f"    private final {service.name}Grpc.{service.name}FutureStub {service.field}Future;"
        )
    lines.extend(
        [
            "",
            "    public static TalonClientset create(Channel channel) {",
            "        return new TalonClientset(channel);",
            "    }",
            "",
            "    public TalonClientset(Channel channel) {",
            "        this.channel = channel;",
        ]
    )
    for service in services:
        lines.append(
            f"        this.{service.field} = {service.name}Grpc.newBlockingStub(channel);"
        )
        lines.append(f"        this.{service.field}Async = {service.name}Grpc.newStub(channel);")
        lines.append(
            f"        this.{service.field}Future = {service.name}Grpc.newFutureStub(channel);"
        )
    lines.extend(
        [
            "    }",
            "",
            "    public Channel channel() {",
            "        return channel;",
            "    }",
            "",
        ]
    )
    for service in services:
        java_field = service.field
        java_accessor = service.field
        lines.extend(
            [
                f"    public {service.name}Grpc.{service.name}BlockingStub {java_accessor}() {{",
                f"        return {java_field};",
                "    }",
                "",
                f"    public {service.name}Grpc.{service.name}Stub {java_accessor}Async() {{",
                f"        return {java_field}Async;",
                "    }",
                "",
                f"    public {service.name}Grpc.{service.name}FutureStub {java_accessor}Future() {{",
                f"        return {java_field}Future;",
                "    }",
                "",
            ]
        )
    lines.extend(["}", ""])
    write(JAVA_OUT, "\n".join(lines))


def service_field_name(domain: str) -> str:
    if domain == "Auth":
        return "auth"
    if domain == "Knowledge":
        return "knowledge"
    return pluralize(to_camel_case(domain))


def pluralize(value: str) -> str:
    if value.endswith("y"):
        return value[:-1] + "ies"
    if value.endswith(("s", "x", "ch", "sh")):
        return value + "es"
    return value + "s"


def to_camel_case(value: str) -> str:
    pascal = to_pascal_case(value)
    return pascal[:1].lower() + pascal[1:]


def to_pascal_case(value: str) -> str:
    return "".join(part[:1].upper() + part[1:] for part in value.split("_"))


def write(path: Path, source: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(source.rstrip() + "\n", encoding="utf-8")


if __name__ == "__main__":
    main()
