use anyhow::Result;
use std::fs;

use super::{Cli, RunOutcome};
use crate::cli::parse_raw_manifest;
use crate::control::resource_model::TypedResource;

#[derive(clap::Args)]
pub(crate) struct GenCommand {
    #[arg(long, default_value = "conic/manifests")]
    dir: String,
    #[arg(long, default_value = "client.ts")]
    out: String,
}

pub(super) async fn run(_cli: &Cli, command: &GenCommand) -> Result<RunOutcome> {
    println!("Generating Talon Client SDK from: {}", command.dir);
    let class_methods = sdk_methods_from_dir(&command.dir)?;

    let full_file = format!(
        r#"// Auto-generated by talon-cli gen
export class TalonClient {{
  constructor(private endpoint: string) {{}}

{}
}}
"#,
        class_methods.join("\n\n")
    );

    fs::write(&command.out, full_file)?;
    println!("Wrote API client to {}", command.out);
    Ok(RunOutcome { exit_code: None })
}

fn to_camel_case(s: &str) -> String {
    let mut result = String::new();
    let mut capitalize_next = true;
    for c in s.chars() {
        if c == '-' || c == '_' {
            capitalize_next = true;
        } else if capitalize_next {
            result.extend(c.to_uppercase());
            capitalize_next = false;
        } else {
            result.push(c);
        }
    }
    result
}

fn sdk_methods_from_dir(dir: &str) -> Result<Vec<String>> {
    let mut class_methods = Vec::new();
    for entry in fs::read_dir(dir)? {
        let entry = entry?;
        let path = entry.path();
        let ext = path.extension().unwrap_or_default();
        if ext != "yaml" && ext != "yml" {
            continue;
        }
        let content = fs::read_to_string(&path)?;
        let raw = parse_raw_manifest(&content)?;
        if raw.kind == "Agent" {
            let agent = crate::control::manifest::parse_agent(&content)?;
            let method_name = format!("create{}", to_camel_case(&agent.name()));
            class_methods.push(format!(
                r#"  async {method_name}(workspaceId: string): Promise<any> {{
    return fetch(`${{this.endpoint}}/api/agents`, {{
      method: "POST",
      headers: {{ "Content-Type": "application/json" }},
      body: JSON.stringify({{ agent: "{raw_name}", namespace: workspaceId, inputs: {{}} }})
    }}).then(r => r.json());
  }}"#,
                method_name = method_name,
                raw_name = agent.name(),
            ));
        }
    }
    Ok(class_methods)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn sdk_methods_from_dir_accepts_yml_manifests() {
        let dir = tempfile::tempdir().expect("create temp dir");
        let manifest = dir.path().join("agent.yml");
        fs::write(
            manifest,
            r#"apiVersion: talon.impalasys.com/v1
kind: Agent
metadata:
  name: sdk-agent
  namespace: default
spec: {}
"#,
        )
        .expect("write manifest");

        let methods = sdk_methods_from_dir(dir.path().to_str().expect("utf-8 path"))
            .expect("generate SDK methods");

        assert_eq!(methods.len(), 1);
        assert!(methods[0].contains("async createSdkAgent("));
    }
}
